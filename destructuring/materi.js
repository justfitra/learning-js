// Destructuring Array

const fruits = ["apel", "mangga", "pisang"];
const [a, b, c] = fruits;

console.log(a); // apel
console.log(b); // mangga

// Destructuring Object

const user = { name: "Fitra", age: 19, city: "Trenggalek" };
const { name, age } = user;

console.log(name); // Fitra
console.log(age); // 19
